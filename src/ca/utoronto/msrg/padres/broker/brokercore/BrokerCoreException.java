package ca.utoronto.msrg.padres.broker.brokercore;

public class BrokerCoreException extends Exception {

	private static final long serialVersionUID = -421728761716570277L;

	public BrokerCoreException() {
	}

	public BrokerCoreException(String arg0) {
		super(arg0);
	}

	public BrokerCoreException(Throwable arg0) {
		super(arg0);
	}

	public BrokerCoreException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
